package org.tensorflow.lite.examples.objectdetection;

public class gem {
    String email;
    String gemName;
    String gemWeight;
    String gemShape;
    String perCarat;
    String gemPrice;

    String imagePath;

    public String getImagePath() {
        return imagePath;
    }

    public void setImagePath(String imagePath) {
        this.imagePath = imagePath;
    }

    public String getGemName() {
        return gemName;
    }

    public void setGemName(String brandID) {
        this.gemName = brandID;
    }

    public String getGemWeight() {
        return gemWeight;
    }

    public void setGemWeight(String brandName) {
        this.gemWeight = brandName;
    }

    public String getGemShape() {
        return gemShape;
    }

    public void setGemShape(String sellerName) {
        this.gemShape = sellerName;
    }

    public String getPerCarat() {
        return perCarat;
    }

    public void setPerCarat(String address) {
        this.perCarat = address;
    }

    public String getGemPrice() {
        return gemPrice;
    }

    public void setGemPrice(String contactNumber) {
        this.gemPrice = contactNumber;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.gemPrice = email;
    }

}